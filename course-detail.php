                    <section class="mb-6" id="description" class="mb-6">
                    <p>This qualification reflects the job roles of workers in the accounting industry, including BAS Agents and contract bookkeepers; and of those employees performing bookkeeping tasks for organisation in a range of industries. It includes preparing and lodging business and instalment activity statements and providing advice or dealing with the Commissioner on behalf of a taxpayer in relation to activity statements.</p>

                    <p>Individuals in these roles apply theoretical and specialist knowledge and skills to work autonomously, and exercise judgement in completing routine and non-routine activities.</p>
                    </section>
                    <section class="mb-6" id="outcome">
                        <h4 class="text-green">Outcome</h4>
                        <p>This qualification reflects the role of individuals who use well-developed skills and a broad knowledge base in a wide variety of contexts and environments. They apply solutions to a defined range of problems, and analyse and evaluate information from a variety of sources in relation to a variety of tasks. They may provide guidance to others with some limited responsibility for the output of others. This qualification is designed to reflect the role of contract bookkeepers and employees performing in the role of bookkeeper for organizations and who perform duties such as:</p>
                        <ul>
                            <li>establishing and maintaining accounting systems</li>
                            <li>assisting with Business Activity Statements and other office taxes</li>
                            <li>payroll</li>
                            <li>developing management systems for organisation</li>
                        </ul>
                    </section>

